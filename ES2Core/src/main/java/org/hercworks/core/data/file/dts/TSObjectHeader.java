package org.hercworks.core.data.file.dts;

import java.util.Arrays;

public enum TSObjectHeader {

	TS_POLY(new byte[]{0x01,0x00,0x14,0x00}),
	
	TS_SOLID_POLY(new byte[]{0x02,0x00,0x14,0x00}),
	
	TS_SHADED_POLY(new byte[]{0x03,0x00,0x14,0x00}),
	
	TS_BASE_PART(new byte[]{0x05,0x00,0x14,0x00}),
	
	TS_PART_LIST(new byte[]{0x07,0x00,0x14,0x00}),
	
	TS_SHAPE(new byte[]{0x08,0x00,0x14,0x00}),

	TS_GOURAUD_POLY(new byte[] {0x09,0x00,0x14,0x00}),
	
	TS_BSP_GROUP(new byte[] {0x0a,0x00,0x14,0x00}),
	
	TS_CELL_ANIM_PART(new byte[]{0x0b,0x00,0x14,0x00}),
	
	TS_DETAIL_PART(new byte[] {0x0c,0x00,0x14,0x00}),
	
	TS_TEXTURE4_POLY(new byte[]{0x0f,0x00,0x14,0x00}),

	TS_GROUP(new byte[]{0x14,0x00,0x14,0x00}),
	
	ALIAS_SOLID_POLY(new byte[] {0x10,0x00,0x14,0x00}),
	
	ALIAS_SHADED_POLY(new byte[] {0x11,0x00,0x14,0x00}),
	
	ALIAS_GOURAUD_POLY(new byte[] {0x12,0x00,0x14,0x00}),
	
	TS_BITMAP_PART(new byte[] {0x13,0x00,0x14,0x00}),
	
	BSP_PART(new byte[]{0x15,0x00,0x14,0x00}),
	
	AN_SEQUENCE(new byte[]{0x01,0x00,0x1e,0x00}),
	
	AN_ANIM_LIST(new byte[]{0x02,0x00,0x1e,0x00}),
	
	AN_SHAPE(new byte[]{0x03,0x00,0x1e,0x00}),
	
	AN_CYCLIC_SEQUENCE(new byte[]{0x04,0x00,0x1e,0x00})
	;	
	
	private final byte[] val;
	
	private TSObjectHeader(byte[] val) {
		this.val = val;
		
	}
	
	public byte[] val() {
		return this.val;
	}
	
	public static TSObjectHeader findVal(byte[] marker) {
		for(TSObjectHeader hdr : TSObjectHeader.values()) {
			if(Arrays.equals(hdr.val(), marker)) {
				return hdr;
			}
		}
		return null;
	}
}
