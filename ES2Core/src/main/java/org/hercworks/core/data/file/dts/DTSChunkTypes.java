package org.hercworks.core.data.file.dts;

/**
 * mappings based on:
 * https://github.com/booto/convert_dts
 * https://github.com/open-siege/siege-studio/blob/master/siege-modules/siege-content/src/dts/3space.cpp
 */
public enum DTSChunkTypes {
	
	NULL_OBJ(new byte[] {0x0E, 0x00, 0x14, 0x00}),
	POLY(new byte[] {0x01, 0x00, 0x14, 0x00}),
	SOLID_POLY(new byte[] {0x02, 0x00, 0x14, 0x00}),
	TEXTURE4_POLY(new byte[] {0x0F, 0x00, 0x14, 0x00}),
	SHADED_POLY(new byte[] {0x03, 0x00, 0x14, 0x00}),
	GOURAD_POLY(new byte[] {0x09, 0x00, 0x14, 0x00}),
	ALIAS_SOLID_POLY(new byte[] {0x10, 0x00, 0x14, 0x00}),
	ALIAS_SHADE_POLY(new byte[] {0x11, 0x00, 0x14, 0x00}),
	ALIAS_GOURAD_POLY(new byte[] {0x12, 0x00, 0x14, 0x00}),
	GROUP(new byte[] {0x14, 0x00, 0x14, 0x00}),
	BSP_GROUP(new byte[] {0x0A, 0x00, 0x14, 0x00}),
	BASE_PART(new byte[] {0x05, 0x00, 0x14, 0x00}),
	BMP_PART(new byte[] {0x013, 0x00, 0x14, 0x00}),
	PART_LIST(new byte[] {0x07, 0x00, 0x14, 0x00}),
	CELL_ANIM_PART(new byte[] {0x0B, 0x00, 0x14, 0x00}),
	DETAIL_PART(new byte[] {0x0C, 0x00, 0x14, 0x00}),
	BSP_PART(new byte[] {0x15, 0x00, 0x14, 0x00}),
	TS_SHAPE(new byte[] {0x08, 0x00, 0x14, 0x00}),
	SEQ(new byte[] {0x01, 0x00, 0x1E, 0x00}),
	CYCLIC_SEQ(new byte[] {0x04, 0x00, 0x1E, 0x00}),
	ANIM_LIST(new byte[] {0x02, 0x00, 0x1E, 0x00}),
	ANIM_SHAPE(new byte[] {0x03, 0x00, 0x1E, 0x00}),
	//DC_SHAPE(new byte[] {0xF8, 0x00, 0x1E, 0x00}),
	//DC_SHAPE_INST(new byte[] { 0xF5, 0x01, 0xBC, 0x02 })
	
	;
	/*

	 */
	
	
	private byte[] marker;
	
	private DTSChunkTypes( byte[] marker) {
		this.marker = marker;
	}

	public byte[] marker() {
		return marker;
	}
	
	public static DTSChunkTypes fromChunk(byte[] c) {
		
		for(DTSChunkTypes t : DTSChunkTypes.values()) {
			if(t.marker.equals(c)) {
				return t;
			}
		}
		
		return null;
	}
	
}
